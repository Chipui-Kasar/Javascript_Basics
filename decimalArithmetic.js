var decimalNumber = 1.1;
console.log("add decimal number : " + (decimalNumber + 2.2).toFixed(3)); //.tofixed is to limit the numbers
console.log("subtract decimal number : " + (decimalNumber - 0.2).toFixed(2));
console.log("Multiple decimal number : " + (decimalNumber * 3.3).toFixed(4));
console.log("Divide decimal number : " + (4.4 / 2.2).toFixed(2));
